// Ejemplo básico de función
// la funcion espera que le pasemos una variable nombre 
function saludar(nombre) {
  console.log("¡Hola, " + nombre + "!");
}
// Llamada a la función
saludar("Juan");

// ejemplo 2 de function
// la funcion espera numero 1 y numero 2 para retornar la suma
function suma2Numeros(numero1, numero2) {
  return numero1 + numero2;
}
console.log(suma2Numeros(2, 4));

