// ******* VARIABLES ******* \\

// * Variables en JavaScript: Almacenan y representan valores.
let nombre = "Juan";
const edad = 25;
var casado = false;

// let permite crear una variable
// una variable puede cambiar su valor, por eso su nombre
nombre = "Luis"
console.log(nombre)

// const no permite variar su valor, porque es una constante
edad = 56

// var tambien puede cambiar su valor, pero la diferencia es el scope del valor
// var es un valor que puede ser accedido desde cualquier parte de la aplicacion
// let solo es accesible en bloques donde se asigna
casado = true
console.log(casado.toString())